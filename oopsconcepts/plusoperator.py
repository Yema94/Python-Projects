# operator overloading in Polymorphism
# "+" operators reacts differently based on the data type it handles
# "+" operator is polymorphic
# It overloads based on the data type it handles

a = 10
b = 20
# mathematical addition operation
print(a+b)

s1 = "Hello"
s2 = "How are you?"
# string concantenation
print(s1 + s2)

lst1 = [1, 2, 3]
lst2 = [4, 5, 6]
# combines two lists and gives new list
print(lst1 + lst2)